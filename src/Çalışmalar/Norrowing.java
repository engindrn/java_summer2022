package Çalışmalar;

public class Norrowing {
    public static void main(String[] args) {

         double sayi= 255.36;

         int sayi1=(int) sayi;

         byte sayi2=(byte)sayi1;

        System.out.println(sayi);
        System.out.println(sayi1);
        System.out.println(sayi2);

    }
}
